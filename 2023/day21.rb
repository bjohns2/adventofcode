def parse_input(input)
  input.split("\n").map{|l| l.split('')}
end

def print_map(map, os)
  map.each_with_index do |line, i|
    new_line = line.map.with_index do |char, j|
      os.include?([i,j]) ? 'O' : char
    end
    puts new_line.join('')
  end
end

def find_start(map)
  map.each_with_index do |line, i|
    line.each_with_index do |char, j|
      return [i,j] if char == 'S'
    end
  end
end

def step(map, os)
  new_os = []
  os.each do |o|
    if o[0]-1 >= 0
      up = map[o[0]-1][o[1]]
      new_os << [o[0]-1,o[1]] if up != '#'
    end

    if o[0]+1 < map.size
      down = map[o[0]+1][o[1]]
      new_os << [o[0]+1,o[1]] if down != '#'
    end

    if o[1]-1 >= 0
      left = map[o[0]][o[1]-1]
      new_os << [o[0], o[1]-1] if left != '#'
    end

    if o[1]+1 < map[0].size
      right = map[o[0]][o[1]+1]
      new_os << [o[0], o[1]+1] if right != '#'
    end
  end
  new_os
end

def solution(input)
  map = parse_input(input)

  start = find_start(map)

  os = [start]

  (0..64).each do |i|
    puts "step #{i} has #{os.size} Os"
    os = step(map, os).uniq
    # print_map(map, os)
  end
end

