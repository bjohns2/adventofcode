def format_input(input)
  map = input.split("\n")
  map.map do |line|
    line.split('')
  end
end

def shift_rocks(map)
  map.map do |line|
    str_line = line.join('')
    line_sections = str_line.split('#',-1)
    new_sections = line_sections.map do |line_section|
      rock_count = line_section.count('O')
      empty_count = line_section.count('.')
      '0' * rock_count + '.' * empty_count
    end
    new_sections.join('#').split('')
  end
end

def count_load(map)
  total_load = 0
  map.each do |line|
    line_size = line.size
    line.each_with_index do |char, i|
      total_load += (line_size - i) if char == '0'
    end
  end
  total_load
end

def print_map(message, map)
  printed_map = map.map{|line| line.join('')}.join("\n")
  puts "#{message}: \n#{printed_map}"
end

def solution(input)
  map = format_input(input)
  # print_map('map',map)

  transposed_map = map.transpose
  # print_map('transposed map',transposed_map)

  shifted_map =  shift_rocks(transposed_map)
  # print_map('shifted map',shifted_map)

  total_load = count_load(shifted_map)
  puts total_load
end

